    private void jTablePacientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTablePacientesMouseClicked
        int column = jTablePacientes.getColumnModel().getColumnIndexAtX(evt.getX());
        int row = evt.getY() / jTablePacientes.getRowHeight();
        Paciente pacienteController = new Paciente();
        if (row < jTablePacientes.getRowCount() && row >= 0 && column < jTablePacientes.getColumnCount() && column >= 0) {
            Object value = jTablePacientes.getValueAt(row, column);
            if (value instanceof JButton && conteoClick == 0) {
                conteoClick++;
                ((JButton) value).doClick();
                JButton boton = (JButton) value;
                boton.setEnabled(false);

                PacienteBo paciente = pacienteController.getPacienteWherId(boton.getName());

                JFPerfilPaciente panel = new JFPerfilPaciente(paciente, medico);
                panel.setVisible(true);
                this.setVisible(false);

            }

        }        // TODO add your handling code here:
    }//GEN-LAST:event_jTablePacientesMouseClicked

